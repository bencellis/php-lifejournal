<p>One year and eight months later, I have come full circle again.</p>
<p><!--more--> </p>
<p>When I last updated this blog, we were going through the process of selling our business and I took the blog off line because I felt I could not keep it up with all the work and stress.  After all, after that I would be able to concentrate on sorting myself out.  Well it took until April to finalise the deal - all the time, maintaining my habits.  Now over a year later, I have full circle and realise that I have to do something.  So much has happened and I suspect I will talk about this missed period over the next few posts.   That is not to say that I have not been worried by the issues, it is just I keep putting it off until tomorrow and now, tomorrow has come.  Time to change is here.</p>
<p></p>
<p>I have been here before, even before the time I set up the blog.  This really has to be the last time.  This is the time, the changes will be made and made permanent.  It is sad to think that at the springly age of forty two,  I am just as messed up as when I was twenty.  Oh yes, I have the house, the family and a reasonably well paid job but personally I feel unsatifisfied, uncontent.  I have to admit that change does not come easy for me, the change of job and work location (involving an extra two hours commute everyday) have not been followed by other changes that, maybe, I should have made at the time both externally and internally.</p>
<p></p>
<p>But change has to start somewhere, hence the revival of this blog and my <a href="http://songsofwords.mukudu.net/">poetry</a> site.  True the poetry is old but I have a stack of new stuff that I have to add.  I also intend to continue the <a href="http://ellisfamily.mukudu.net">genealogy and related stuff</a> I have been doing on and off for a couple of years.  The intention to do something more productive than drinking and smoking.  These sites are to be my spiritual healing, my praying and my emotional outlet.</p>
<p></p>
<p>There are other changes in the offing including moving house.</p>
<p></p>
<p>Being my first post in such a long time, this starts my commitment to spend a little time hopefully everyday to evaluate where I am going for the goal is noble - contentment.  My feeling about that has not changed since I wrote <a href="http://www.mukudu.net/talking2myself/index.php/about-this-blogpod/">this page</a>.</p>
<p></p>
<p>Love and peace, may you find the balance.</p>

