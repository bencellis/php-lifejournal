<p>England, as many of us are aware, is now 'smoke free'.<!--more-->  The Governement has spent millions telling us this on billboards and advertising which really is not I want them using my tax money on but that's another story.  I haven't been able to smoke in a work building for years and so that's nothing new.  I have always avoided smoking in restaurants, unless others are smoking, prefering to retire to the bar or even outside.  The pub ban is going to hurt especially in winter and maybe even this summer considering the ridiculous amount of rain - yes, I know it always rains in England - we have been having lately.  I must admit I haven't paid much notice to the government funded campaign but now that it is in effect, I am seriously considering stopping smoking.   Many may say that it is the ban working, I beg to differ, I have been considering stopping for ages but the ban may be the catalyst to ensuring I stay not smoking because it is the pub that is always the hardest part.</p>
