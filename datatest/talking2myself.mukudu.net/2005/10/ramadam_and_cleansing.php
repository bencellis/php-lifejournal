<p>In my hunt for tobacco related blogs to link to, I was not surprised to find quite a few documenting attempts to stop smoking.  Unfortunately not one was solely about giving up the dreaded drug.  I'd love to find a humourous account of the harrowing experience.<!--more--></p>
<p></p>
<p>My search via technorati introduced me to the 'spam blog'.  I understand that the term has been used for spammers posting comments to valid blogs but what I discovered is that there seems to be a few blogs that are constantly updated with the same material, most probably by some sort of automated system, which ensures they remain in the blog directories.  Nicotine therapies obviously are big business.  Is there no end to the lengths these people will go to gain eyeballs?</p>
<p></p>
<p>Anyway, wading through the herbal remedies, replacement therapy and everything in between, I found out that Muslims during Ramadam give up not only food but the smokes as well.  I read an excellent <a href="http://bumming4inspiration.blogspot.com/2005/10/happy-meal-for-soul-mcmeaning-of-life.html">blog posting</a>  about this.  It got me thinking though that, maybe, there is a point to this.  Nothing can make you appreciate things a little bit more like abstention (absence makes the heart grow fonder). Of course, if I was one, I would take the opportunity give up smoking at this time but the fasting, I think, can must be good for the spirit. I suppose the most serious dieters would point out that your body would go into 'starvation mode' and store all available fat and water you eat outside the fasting period.  Christians are also supposed to give up something in Lent, though I suppose it is possible that originally, it was probably as strict as Ramadam.</p>
<p></p>
<p>But then this might be the point.  We have grown so accustomed to always having that out bodies do not have the chance to call on reserves and so we get fat and obese.  Far more important, I believe, is the spiritual thanksgiving (whether Muslim or Christian) that comes from doing without. Not being religious though, I think apart from appreciating these things we have, it also allows us to feel a connection to the millions that go without every day and not from choice.  Maybe then, we would not change the channel on the TV when we hear another appeal for help!</p>

