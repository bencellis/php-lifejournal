<p>I suppose that the common view is that alcoholics drink until they fall over.  Is it true?  Can at least functional alcoholics have certain limits, because I seem to?<!--more--></p>
<p></p>
<p>I seem to have a normal upper limit of 6 to 8 pints of lager.  I do, on quite a few occasions, have more than this especially where the drinking period is extended for example at parties.  What seems to be the factor that sort of stops me is tiredness.  My normal bedtime is between 10pm and midnight and once sleeps calls; drink is out of the window.  By this time, I am not normally blind drunk and many times feel fairly sober.  In fact, I can't think of a recent occasion where I was very drunk.</p>
<p></p>
<p>What seems to be one of the governing factor of whether I feel drunk or not is the pace at which I consume the alcohol.  My normal routine is that the first two drinks are consumed fairly rapidly, about a drink every 20-25 minutes.  Thereafter, it slows to about one every 35-45 minutes though I a number of times, I have averaged 1 an hour.  If I drink faster such as at parties then I tend to get drunk.</p>
<p></p>
<p>When I do drink over an extended period, say over from a weekend afternoon to bedtime, my drink rate is much slower than normal and that is usually because I am doing other things at the same time.  On those occasions I normally don't get 'bladdered'.</p>
<p></p>
<p>Another factor is food.  Eating before drinking is best followed by eating whilst drinking.  A lot of times though I eat after the drink.  What I do do is eat, whether I am drunk or not.  Rarely do I go to bed without food.  I also drink lots of water during the night.  I have always had this habit for as long as I can remember.</p>
<p></p>
<p>Now with the fact that I am occasionally plastered, (Isn't it funny, how drinkers have so many ways to describe the level of drunkenness?) am rarely hung over, seem to have some limit, albeit higher than the daily recommended limit,  and can do without drink for a day or two, can I claim to have a drinking problem?</p>

