<p>My period of feeling unhealthy after the DVT saga was emphasised when my doctor suggested I should lose weight and I underestimated my weight in front of my work colleagues.  Some story on the BBC news site about BMI (Body Mass Index) was the pivotal point.<!--more-->  I came home that evening and weighed myself to be shocked at 104kgs. (I don't do pounds or stones, I was educated to the metric standard).  At 180cms high (no feet and inches either) I was just about obese!  Maybe that would explain why I felt so unhealthy.  A bit of winding from my colleagues and I set about to find a plan, maybe like the one I plan now.</p>
<p></p>
<p>Then I caught chicken pox and did get it bad, even the GP drew her breath when she saw me.  Mum always said I have had chicken pox but she had said that about my brother who had it about two years earlier.  How would she have known anyway, both of us where stuck in some bush boarding school at the normal age when kids get it?  For four years or so, she only saw us at Christmas.  Lots of kids at school, I remember, had chicken pox and even German measles but I don't remember having it.  I got chicken pox from my son, the last born, after my three elder children all had a bout.  Did I catch it because I was unhealthy at the time?  I didn't feel very stressed about the chicken pox but it did knock me out for a while.  The upshot was when it was all over I was 98kgs.</p>
<p></p>
<p>I started to exercise to keep my weight going in the right direction for a while.  I discovered Tai-bo (fight like routines) and pilates.   I also re-discovered my love for swimming.  I am an early riser and so I did my exercise in the mornings (alternating pilates and Tai-bo) and finished with a 20mins swim.  It played havoc with my family's and my morning routine and the swimming had to go a few weeks later.  A few weeks later and nothing was gone, I was still hovering round the 98kg mark.  It was then I knew I was going to have to diet as well.  I can't remember particularly any change in my drinking habits then, so it suggests, it was business as usual.</p>

