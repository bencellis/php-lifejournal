<p>Today seems to have been my day for seeing the results of a crazy and inefficient immigration/asylum system in the UK and I suspect all over Europe.  The last election was dogged by talk of immigration and asylum that drew attention from the things we in England should have been pressing the politicians about.  This paranoia about some 2% of the population is stupid!  If our emigrating citizens where being treated in the way we treat people coming to our shores, we would be in an uproar.<!--more--></p>
<p></p>
<p>Anyway, the news, this morning, reported the decision by the Asylum And Immigration Tribunal that Zimbabweans do face a risk of persecution if returned to Zimbabwe on failing an asylum claim.  If you are into such issues, the <a href="http://www.iaa.gov.uk/cg_determinations/country_guidelines/cg_list/aa_04507_2005_zimbabwe.pdf">decision</a> makes interesting reading.  Now this doesn't affect me directly as I am a citizen but I do know quite a few Zimbabweans who have had to face the asylum or the immigration system and let me tell you it sucks!</p>
<p></p>
<p>It is funny how until someone is directly affected by a law that they come to realise that sometimes we accept the Government is doing the best for us.  How many cases have there been where men have married foreign women and then found they can't get them into the country?  If an Englishman decides he wants to marry a young Thai or Philippine woman to spend the rest of his life with, why should the Government tell him he can't?  Yes, it may be a marriage of convenience, but aren't many marriages just that?</p>
<p></p>
<p>One of my friends is in love with someone has failed in their asylum case and is under the threat of deportation to Zimbabwe.  He has come face to face with the heart-acre, insecurity and sheer bureaucracy that is this system. Today's decision should have been good news for the couple but an incident, this evening at the pub, displayed the frustration and insecurity these people have to live with on daily basis.  Until there is a clear description of what this decision means to these people, the uncertainty will continue to do their lives.  And because, basically, they are people with no rights with very little access to people who can defend them, they will continue to be a small, silent, suffering community hidden within our society.  Not a healthy situation!</p>

