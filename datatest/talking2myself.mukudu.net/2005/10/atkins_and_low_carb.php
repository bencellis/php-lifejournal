<p>Few weeks of non success leads to the contemplation of another 'plan'.  At the time there was a few articles about the 'Atkins diet'.  Now I am not the type of person who takes things at face value and since I was in the process of thinking about loss weight and diets, I had a look and I was nearly convinced.<!--more--></p>
<p></p>
<p>Why do I love the idea of low carb?  It has something to do with my African upbringing I suppose.  Meat, meat, fish, nuts, berries, fruit, vegetables and meat just made sense based on what I believe the human body has evolved to digest.  And water.  (Can you tell I love meat?)</p>
<p></p>
<p>A few things put me off the Atkins diet and other low carb diets I discover in my research.  </p>
<p>* counting of carbs _ I believe you should eat until satisfied.  If you eat the right food, should it matter? </p>
<p>* supplements  - if you need supplements to complement your diet, then you are not eating well.</p>
<p>* what have been described as 'pretend carbohydrates' obviously gracefully supplied by Atkins Inc. If you need to pretend to eat chocolate or whatever, then you might as well join the 'functional addict' club!</p>
<p>* forcing your body to go onto fat eating mode, lipolysis and ketosis.  If you eat right, then, do you need to do this?</p>
<p> </p>
<p>By far the most common sense low carb variation is one evangelised by a British man, David Mills with his carbWiser plan (<a href="http://www.low-carbdiet.co.uk/">http://www.carbwiser.com/</a>).  It is that plan with my variations that I followed for over the next 8 or so months.  My guiding principle with my diet was simple, if you cannot eat it raw or it's just sweet for nothing, don't eat it.</p>
<p></p>
<p>I went from 98kgs in Sept 03 to at my lowest of 89kgs in Apr 04, some 9kgs in 7 months!  I also joined the brilliant forum at <a href="http://www.low-carbdiet.co.uk/">http://www.low-carbdiet.co.uk/</a>.   During this time I kept my exercise routine going and soon I put on some weight from muscle mass I expect.  I am now about 93 kgs but wearing size 36 trousers as opposed to 44 when I started.  Using clothes sizes is a better measure than weight because muscle is denser than fat.</p>
<p></p>
<p>But the devil drink had other ideas.  Beer always featured in my diet.  Slowly the exercise routines faded away, especially after Christmas and more rubbish food returned to my diet.  I still try and avoid some foods or limit my intake of rubbish food and that may suggest the reason for the slow weight gain being experienced now. </p>

