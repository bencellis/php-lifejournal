<p>Friday's incident with my friend and his asylum seeking girlfriend came back to haunt me again.  I found myself between two warring factions and I ended up feeling like I needed asylum. <!--more-->Anyway peace returned in the evening over a few cans of beer, though they were both worse for wear when they came round.</p>
<p></p>
<p>Our system here leaves too many people vulnerable to unscrupulous people.  Whereas you can expect that if the authorities don't know about a person, e.g. an illegal immigrant, very little can be done to protect them, those who are known to the system should have more done to protect them.  In some instances, asylum seekers can get some help for accommodation and living expenses but when you consider the sheer numbers, it is the authorities' interests if these people are afforded help by friends and family who have some right to be here.  Many asylum seekers also know that if you are be supported by the system, there are two problems, one is that there is a vested interest by the authorities to get rid of you and secondly that should your claim fail, you have the greatest chance of being removed.</p>
<p></p>
<p>The problem is the whole process can take months and months and add in any appeals etc and you could be looking at a year.  For a family assisting another this puts a major strain on any resources they have access to.  Some of these families are successful asylum seekers who, in most cases, have low paid jobs and are struggling to make ends meet.  The prospective asylum seekers are not allowed to work but are expected for example, to travel to an immigration office and 'sign on' in some cases, every week.  Where is this money to come from?</p>
<p></p>
<p>It is from these pressures that abuse becomes an issue.  The asylum seeker is now under pressure to find means of support and there are many unscrupulous employers ready to take them on.  Yet, there are industries in this country that are crying out for labour and even law abiding businesses can find themselves skirting with the law in order to continue surviving.  Everyone in these cases is being criminalized by this system.</p>
<p></p>
<p>In other cases, the sheltering family takes advantage of this situation. Most of us have heard or read in the news a number of cases, where people have become virtually slaves.  Physical and mental abuse is not unheard of especially where children are concerned. </p>
<p></p>
<p>Whilst you might argue that their situation here is better than what they are running from, it does not make it right!  We are a civilised and tolerant society that must face the fact no matter what we try and do, human nature will always result in people seeking shelter from those who would hurt them and we have a duty, as human beings, to afford them some help.  But leaving them in limbo with no means to ensure their safety and well-being is a recipe for disaster. </p>

